local errorTextureName = "___error"
local function findMatIndex(entity, findTextureName, tableObject)
	local modelName = entity:GetModel() and entity:GetModel():lower() or ""
	
	if (nut.outfit.indexCache[modelName] and nut.outfit.indexCache[modelName][findTextureName]) then
		return nut.outfit.indexCache[modelName][findTextureName]
	end

	local matIndex
	local errorCorrection = 0
	for k, v in ipairs(entity:GetMaterials()) do
		if (v == errorTextureName) then
			errorCorrection = errorCorrection + 1
		elseif (v == findTextureName) then
			if (CLIENT) then
				matIndex = k - 1
			else
				matIndex = k - 1 - errorCorrection
			end

			break
		end
	end

	if (matIndex) then
		nut.outfit.indexCache[modelName] = nut.outfit.indexCache[modelName] or {}
		nut.outfit.indexCache[modelName][findTextureName] = matIndex
	end

	return matIndex
end

local function getFacemaps(modelName, defaultPrice, changePrice)
	if (modelName) then
		local outfitData = OUTFIT_DATA[modelName:lower()]

		if (outfitData) then
			local result, default, facemaps = {}, outfitData.facemap, outfitData.skins

			table.insert(result, {mat = default, price = (defaultPrice or 9500)})
			if (facemaps) then
				for _, mat in ipairs(facemaps) do
					table.insert(result, {mat = mat, price = (changePrice or 9500)})
				end
			end
			
			return result
		end
	end

	return {}
end

local function changeFace(entity, wearID, outfitData)
	if (IsValid(entity)) then
		local model = entity:GetModel() and entity:GetModel():lower() or ""
		local faceData = OUTFIT_DATA[model]

		if (faceData and faceData.facemap) then
			local outfits = type(outfitData.outfits) == 'function' and outfitData.outfits(entity) or outfitData.outfits
			if (outfitData.outfits) then
				local wearOutfitData = outfits[wearID]

				if (wearOutfitData) then
					local matIndex = findMatIndex(entity, faceData.facemap, outfitData)
					
					if (matIndex and wearOutfitData.mat) then
						entity:SetSubMaterial(matIndex, wearOutfitData.mat)
					end
				end
			end
		end
	end
end

local function changePart(entity, wearID, outfitData)
	if (IsValid(entity)) then
		if (outfitData.bodygroup and outfitData.outfits) then
			local wearOutfitData = outfitData.outfits[wearID]

			if (wearOutfitData) then
				if (wearOutfitData.group) then
					entity:SetBodygroup(outfitData.bodygroup, wearOutfitData.group or 0)
				end
				if (wearOutfitData.mat) then
					local matIndex = findMatIndex(entity, wearOutfitData.find, outfitData)

					if (matIndex) then
						if (CLIENT) then
							entity:SetSubMaterial(matIndex, wearOutfitData.mat)
						else
							entity:SetSubMaterial(matIndex, wearOutfitData.mat)
						end
					end
				end
			end
		else
			print("[-] Outfitter: Abnormal outfit data: " .. outfitData.name .. ".")
		end
	end
end

